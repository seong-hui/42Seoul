#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long	nb;
	char		c;

	nb = (long long)n;
	if (nb < 0)
	{
		write (fd, "-", 1);
		nb = nb * (-1);
	}
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	c = nb % 10 + '0';
	write(fd, &c, 1);
}
